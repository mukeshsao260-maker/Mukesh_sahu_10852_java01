import java.util.*;
public class Question13{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter your Electricity unit: ");
        int unit = sc.nextInt();
        int bill;

        if(unit<=100){
            bill = unit*5;
        }else{
            bill = (100*5) + (unit-100)*10;
        }
        System.out.println("Your bill is: " + bill);
    }
}


