public class Question25{ 
   public static void main(String[] args) { 
       int n = 5; 
       int fact = 1; 
       int sum = 0; 
 
       for(int i = 1; i <= n; i++) { 
           fact *= i; 
           sum += fact; 
       } 
 
       System.out.println("Sum: " + sum); 
   } 
} 




