public class Question18{ 
   public static void main(String[] args) { 
       int n1 = 5, n2 = 2; 
       int max = Math.max(n1, n2); 
 
       for(;; max++) { 
           if(max % n1 == 0 && max % n2 == 0) { 
               System.out.println("LCM: " + max); 
               break; 
           } 
       } 
   } 
} 



