import java.util.*;
public class Question23{ 
   public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); 
       int n = sc.nextint(); 
       int sum = 0; 
 
       for(int i = 1; i <= n; i++) { 
           sum += i * i; 
       } 
 
       System.out.println("Sum: " + sum); 
   } 
} 





