import java.util.*;
public class Question20{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter your number: ");
        int n = sc.nextInt();
        int pro = 1;
        while(n>0){
            int rem = n%10;
            pro = pro*rem;
            n = n/10;
        }
        System.out.println("Answer will be: " + pro);
    }
}


