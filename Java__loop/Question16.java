import java.util.*;
public class Question16{ 
   public static void main(String[] args) {
    Scanner sc = new Scanner(System.in); 
       int n = sc.nextInt(); 
       boolean isPrime = true; 
 
       for(int i = 2; i <= n / 2; i++) { 
           if(n % i == 0) { 
               isPrime = false; 
               break; 
           } 
       } 
 
       if(isPrime && n > 1) 
           System.out.println("Prime"); 
       else 
           System.out.println("Not Prime"); 
   } 
} 



