import java.util.*;
public class Question33{
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter a binary digits: ");
        int b = sc.nextInt();
        int sum = 0;
        int count = 0;
        while(b>0){
            int rem = b%10;
            for(int i=1;i<=count;i++){
                rem = rem*2;
            }
            sum = sum+rem;
            count++;
            b = b/10;
        }
        System.out.print(sum);
    }
}



